        $('.refresh').click(function(e) {
            var h = $(this).parents(".header");
            var p = h.parent();
            var loading = $('&lt;div class="loading"&gt;&lt;i class="fa fa-refresh fa-spin"&gt;&lt;/i&gt;&lt;/div&gt;');

            loading.appendTo(p);
            loading.fadeIn();
            setTimeout(function() {
                loading.fadeOut();
            }, 1000);

            e.preventDefault();
        });

        $('.close-down').click(function(e) {
            var h = $(this).parents(".header");
            var p = h.parent();

            p.fadeOut(function() {
                $(this).remove();
            });
            e.preventDefault();
        });
